// ── 解析输出 ────────────────────────────────────────────────────────────────
function parsePhases(output: string): string[] | null {
  const result: string[] = [];
  for (let i = 0; i < 5; i++) {
    const match = output.match(new RegExp(`<PHASE_${i}>([\\s\\S]*?)</PHASE_${i}>`));
    if (!match) {
      console.error(`  ✗ 缺少 PHASE_${i} 标签`);
      return null;
    }
    result.push(match[1].trim());
  }
  return result;
}

// ── 单个角色处理 ─────────────────────────────────────────────────────────────
async function processCharacter(char: {
  id: string;
  name: string;
  age: number;
  occupation: string;
  personality: string;
  speakingStyle: string;
  background: string;
  storyPhases: any;
}) {
  const currentPhases = char.storyPhases as string[] | null;
  if (!currentPhases || currentPhases.length < 5) {
    console.log(`  ⚠ ${char.name}: 没有现有剧情，跳过`);
    return false;
  }

  console.log(`\n🖊  处理角色：${char.name}`);
  const hasActivity = !!CHARACTER_ACTIVITIES[char.name];
  if (!hasActivity) {
    console.log(`  ℹ  无预设活动锚点，将用通用提示`);
  }

  const prompt = buildPrompt(char, currentPhases);

  let output: string;
  try {
    const res = await grok.chat.completions.create({
      model: 'grok-3',
      messages: [{ role: 'user', content: prompt }],
      max_tokens: 6000,
      temperature: 0.85,
    });
    output = res.choices[0].message.content ?? '';
  } catch (e: any) {
    console.error(`  ✗ Grok API 错误：${e.message}`);
    return false;
  }

  const phases = parsePhases(output);
  if (!phases) {
    console.error(`  ✗ 解析失败，跳过写入`);
    console.log('--- 原始输出前 500 字 ---\n', output.slice(0, 500));
    return false;
  }

  console.log(`  ✓ 解析成功，5个阶段，总长度 ${phases.reduce((a, p) => a + p.length, 0)} 字`);
  phases.forEach((p, i) => {
    const nodes = (p.match(/【节点 P\d/g) ?? []).length;
    console.log(`    P${i}: ${nodes} 节点，${p.length} 字`);
  });

  if (dryRun) {
    console.log('\n--- DRY RUN 输出 ---');
    console.log('\n=== PHASE 0 (前400字) ===');
    console.log(phases[0].slice(0, 400));
    return true;
  }

  await prisma.character.update({
    where: { id: char.id },
    data: { storyPhases: phases as any },
  });
  console.log(`  ✓ 已写入数据库`);
  return true;
}

// ── 主流程 ───────────────────────────────────────────────────────────────────
async function main() {
  console.log('🚀 rewriteStoriesV2 启动');
  if (dryRun) console.log('   模式：DRY RUN（不写入数据库）');
  if (targetName) console.log(`   目标角色：${targetName}`);

  const where: any = { isPublic: true, storyPhases: { not: null as any } };
  if (targetName) where.name = targetName;

  const chars = await prisma.character.findMany({
    where,
    select: {
      id: true, name: true, age: true, occupation: true,
      personality: true, speakingStyle: true, background: true,
      storyPhases: true,
    },
    orderBy: { name: 'asc' },
  });

  console.log(`   找到 ${chars.length} 个角色`);

  let success = 0, fail = 0;
  for (const char of chars) {
    try {
      const ok = await processCharacter(char as any);
      if (ok) success++; else fail++;
      if (!dryRun) await new Promise(r => setTimeout(r, 3000));
    } catch (e: any) {
      console.error(`  ✗ ${char.name} 处理失败：${e.message}`);
      fail++;
    }
  }

  console.log(`\n✅ 完成。成功：${success}，失败：${fail}`);
  await prisma.$disconnect();
}

main().catch(e => { console.error(e); process.exit(1); });
